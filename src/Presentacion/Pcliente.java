    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        ciCliente = txtCiCli.getText();
        nombre = txtNombreCli.getText();
        direccion = txtDireccionCli.getText();
        email = txtEmailCli.getText();
        telefono = txtTelefonoCli.getText();
        if (accion.equals("Guardar")) {
            NegocioCliente.Guardar(ciCliente, nombre, direccion,email,telefono);
            Mostrar();
            limpiar();
            inhabilitar();
            achicarColumnas();
        }else if (accion.equals("Modificar")) {
            NegocioCliente.Modificar(ciCliente, nombre, direccion,email,telefono);
            Mostrar();
            achicarColumnas();
            limpiar();
            inhabilitar();
            txtCiCli.setEnabled(true);
            accion="Guardar";
            btnGuardar.setText(accion);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

