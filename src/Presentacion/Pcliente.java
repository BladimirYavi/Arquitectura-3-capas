    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String ciCliente = txtCiCli.getText();
        if (!txtCiCli.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Esta seguro que desea eliminar?", "Confirmar", 2);
            if (confirmacion == 0) {
                NegocioCliente.Eliminar(ciCliente);
                Mostrar();
                accion="Guardar";
                btnGuardar.setText(accion);
                txtCiCli.setEnabled(true);
                achicarColumnas();
                limpiar();
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tablaClienteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaClienteMouseClicked
        // TODO add your handling code here:
        btnGuardar.setText("Modificar");
        habilitar();
        //btnEliminar.setEnabled(true);
        btnEliminar.setVisible(true);
        accion = "Modificar";
        int fila = tablaCliente.rowAtPoint(evt.getPoint());
        txtCiCli.setText(tablaCliente.getValueAt(fila, 0).toString());
        txtCiCli.setEnabled(false);
        txtNombreCli.setText(tablaCliente.getValueAt(fila, 1).toString());
        txtDireccionCli.setText(tablaCliente.getValueAt(fila, 2).toString());
        txtEmailCli.setText(tablaCliente.getValueAt(fila, 3).toString());
        txtTelefonoCli.setText(tablaCliente.getValueAt(fila, 4).toString());
    }//GEN-LAST:event_tablaClienteMouseClicked

