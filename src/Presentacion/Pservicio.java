    void inhabilitar() {
        txtidServicio.setEditable(false);
        txtnombreServicio.setEditable(false);
        txtcostoServicio.setEditable(false);

        btnGuardar.setVisible(false);
        btnCancelar.setVisible(false);
        btnEliminar.setVisible(false);
        cbArea.setEnabled(false);

    }

    void habilitar() {
        txtidServicio.setEnabled(false);
        txtnombreServicio.setEditable(true);
        txtcostoServicio.setEditable(true);

        btnGuardar.setVisible(true);
        btnCancelar.setVisible(true);
        btnEliminar.setVisible(false);
        cbArea.setEnabled(true);

    }

    void limpiar() {
        txtidServicio.setText("");
        txtnombreServicio.setText("");
        txtcostoServicio.setText("");
        cbArea.setSelectedIndex(0);
    }

