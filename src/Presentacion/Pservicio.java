    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        // TODO add your handling code here:
       // idServicio = Integer.parseInt(txtidServicio.getText());
        nombre = txtnombreServicio.getText();
        costoServ = Double.parseDouble(txtcostoServicio.getText());
        if (accion.equals("Guardar")) {
            NegocioServicio.Guardar(nombre, costoServ, idArea);
            Mostrar();
            limpiar();
            inhabilitar();
            //achicarColumnas();
        } else if (accion.equals("Modificar")) {
            NegocioServicio.Modificar(idServicio,nombre, costoServ, idArea);
            Mostrar();
            achicarColumnas();
            limpiar();
            inhabilitar();
            txtidServicio.setEnabled(true);
            accion = "Guardar";
            btnGuardar.setText(accion);
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

