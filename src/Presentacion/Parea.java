    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        idArea = txtidArea.getText();
        nombre = txtnombreArea.getText();
        telefono = txttelefonoArea.getText();
        
        // Aqui llamamos al metodo Guardar del Negocio Area
        if (accion.equals("Guardar")) {
            Negocioarea.Guardar(idArea, nombre, telefono);
            mostrar();
            limpiar();
            inhabilitar();
        // Aqui llamamos al metodo Modificar del Negocio Area
        }else if (accion.equals("Modificar")) {
            Negocioarea.Modificar(idArea,nombre,telefono);
            mostrar();
            limpiar();
            inhabilitar();
            txtidArea.setEnabled(true);
            accion="Guardar";
            btnGuardar.setText(accion);
        }

    }//GEN-LAST:event_btnGuardarActionPerformed

