    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        String idArea = txtidArea.getText();
        if (!txtidArea.getText().equals("")) {
            int confirmacion = JOptionPane.showConfirmDialog(rootPane, "Esta seguro que desea eliminar?", "Confirmar", 2);
            if (confirmacion == 0) {
                Negocioarea.Eliminar(idArea);
                mostrar();
                txtidArea.setEnabled(true);
                accion="Guardar";
                btnGuardar.setText(accion);
                limpiar();
                inhabilitar();
            }
        }
    }//GEN-LAST:event_btnEliminarActionPerformed

    private void tablaAreaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tablaAreaMouseClicked
        // TODO add your handling code here:
        btnGuardar.setText("Modificar");

        habilitar();
        btnEliminar.setEnabled(true);
        accion = "Modificar";
        int fila = tablaArea.rowAtPoint(evt.getPoint());
        txtidArea.setText(tablaArea.getValueAt(fila, 0).toString());
        txtidArea.setEnabled(false);
        txtnombreArea.setText(tablaArea.getValueAt(fila, 1).toString());
        txttelefonoArea.setText(tablaArea.getValueAt(fila, 2).toString());
    }//GEN-LAST:event_tablaAreaMouseClicked

